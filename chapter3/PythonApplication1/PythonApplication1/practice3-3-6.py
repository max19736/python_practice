#建立一個字串變數內容為'1101 台泥'，指派給s1並印出
s1='1101 台泥'
print("s1=>",s1)

#輸出'1'出現的次數和字串長度
x=len(s1)
print("出現1的次數:",s1.count('1'))
print("字串長度:",x)

#建立出來的變數利用空白字元分割，並用lis1儲存，輸出lis1的長度
s1.split()
lis1=[s1]
y=len(lis1)
print("lis1的長度",y)

#加入'1102','亞泥'在lis1後面，模擬二維陣列lis2
s2='1102 亞泥'
lis2=[[s1,s2]]

print("驗證0-0:",lis2[0][0]) #0之0為台泥
print("驗證0-1:",lis2[0][1]) #0之1為亞泥